using System;

namespace MyCQRS.Domain.Photographers
{
    public class Photographer
    {
        public Photographer(Guid id, string name, string email)
        {
            Id = id;
            Name = name;
            Email = email;
        }

        public Guid Id { get; set; }
        public string Name { get; set; }
        public string Email { get; set; }
    }
}